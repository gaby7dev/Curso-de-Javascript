// comparador estricto.

const puntaje = 1000;

// == igual a...
if( puntaje == '1000' ){
    console.log('Si es igual.');
}else {
    console.log('No es igual.');
} 

// != diferente de...
if( puntaje != '1000' ){
    console.log('Si es diferente.');
}else {
    console.log('No es diferente.');
} 

// comparador estricto.

// === estrictamente igual a...
if( puntaje === '1000' ){
    console.log('Si es estrictamente igual.');
}else {
    console.log('No es estrictamente igual.');
} 

// !== estrictamente diferente de...
if( puntaje !== '1000' ){
    console.log('Si es estrictamente diferente.');
}else {
    console.log('No es estrictamente diferente.');
} 