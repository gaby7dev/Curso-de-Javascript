// Incrementos o Decrementos

let puntaje = 5;

// incrementando de a 1
console.log(puntaje++);
puntaje = 5;
console.log(++puntaje);

puntaje = 5;

// decrementando de a 1

console.log(puntaje--);
puntaje = 5;
console.log(--puntaje);

puntaje = 5;

// incrementando de a 2

puntaje += 2;
puntaje += 2;

// decrementando de a 3

puntaje -= 3;
puntaje -= 3;

console.log(puntaje);