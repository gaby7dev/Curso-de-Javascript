// Comparar Null y Undefined en JS.

// undefined
let numero;

console.log(numero); // la variable está definida pero su valor no.

console.log(typeof numero);

// Null
let numero2 = null;

console.log(numero2); // la variable está definida pero su valor es nulo.

console.log(typeof numero2);

console.log("Null == Undefined: ", null == undefined);
console.log("Null === Undefined: ", null === undefined);
